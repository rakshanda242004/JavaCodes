import java.util.*;
public class switchcode {
    public static void main (String args[]) {
        int number = 9;
        switch (number) {
            case 1 : System.out.println("Samosa");
            break;
            case 2 : System.out.println("Burger");
            break;
            case 3 : System.out.println("mango shake");
            break;
            default : System.out.println("we wake up");
        }
    }
}

